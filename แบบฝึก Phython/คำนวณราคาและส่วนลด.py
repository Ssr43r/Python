price = int(input("กรุณาใส่ราคาสินค้าของท่าน: "))
amount = int(input("กรุณาใส่จำนวนสินค้่าสของท่าน: "))

total = price * amount

print("ราคาทั้งหมดของท่านคือ", total, "บาท")

if amount >= 3:
   total = price * ( amount - 1 )  #หักจำนวนสินค้าออก 1
print("ท่านได้ทำการซื้อสินค้าตามจำนวนโปรโมชั่น\nท่านท่านได้สิทธิ์ฟรีจำนวน 1 ชิ้น: " , total, "บาท")