print('ยินดีต้อนรับเข้าสู่ร้านกาแฟดิบ')
print('หน้านี้ทำไว้เพื่อเช็ค Member level ของลูกค้า')
cups = int(input('ใส่จำนวนแก้วที่ท่านซื้อ'))

if cups >= 20:
    print('คุณคือระดับ:Gold (ฟรีเค้ก 1 ชิ้น)')
elif cups >= 10:
    print('คุณคือระดับ:Silver (รับส่วนลด 10%)')
elif cups >= 1:
    print('คุณคือระดับ:Member (สะสมแต้มต่อ)')
else:
    print('คุณยังไม่มีประวัติการซื้อ')

print('---ขอบคุณที่ใช้บริการครับ---')