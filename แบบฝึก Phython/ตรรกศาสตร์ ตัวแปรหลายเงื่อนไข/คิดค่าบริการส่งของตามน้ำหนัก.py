print('---ยินดีต้อนรับ---')
print('ขนส่งเรามีกำหนดราคาการส่งสินค้าดังนี้\nน้ำหนัก <= 2 kg: ค่าส่ง = 50 บาท (ราคาเหมา)\nน้ำหนัก 2.1 - 10 kg: ค่าส่ง = น้ำหนัก * 40 บาท\nน้ำหนัก > 10 kg: ค่าส่ง = น้ำหนัก * 70 บาท')
weight = float(input('กรุณากรอกจำนวนน้ำหนักของท่าน'))
if weight <= 2:
    cash = 50
elif weight <= 10:
    cash = weight * 40
else:
    cash = weight * 70
print('ราคาที่ท่านต้องจ่ายคือ: ' , cash, 'บาท')